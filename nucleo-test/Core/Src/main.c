void blink(uint16_t count, uint8_t fast)
{
	uint16_t dms = fast
			? (uint16_t) 100
			: (uint16_t) 300;

	for (uint16_t i = 0; i < count; i++)
	{
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
		HAL_Delay(dms);
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
		HAL_Delay(dms);
	}
}

